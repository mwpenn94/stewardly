/**
 * Seeded PRNG for Monte Carlo reproducibility.
 * Mulberry32 generator (period 2^32) + Box-Muller transform.
 */
export function mulberry32(seed: number): () => number {
  let s = seed | 0;
  return () => {
    s = (s + 0x6d2b79f5) | 0;
    let t = Math.imul(s ^ (s >>> 15), 1 | s);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

export function boxMullerSeeded(rng: () => number): number {
  let u = 0, v = 0;
  while (u === 0) u = rng();
  while (v === 0) v = rng();
  return Math.sqrt(-2.0 * Math.log(u)) * Math.cos(2.0 * Math.PI * v);
}

let _counter = 0;
export function defaultSeed(): number {
  return (Date.now() ^ (++_counter * 0x9e3779b9)) >>> 0;
}
